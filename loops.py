# A  for loop example to print the elements in a list one element at a time
print("Printing using a for loop ")
for i in [0,1, 2, 3, 4, 5]:
    print(i)

# A range example to print the elements in a list one element at a time
print("Printing using a range ")
for i in range(10):
    print(i)

# A sequence example to print the elements in a list one element at a time
print("Printing using a sequence ")
names = ["Jeff", "Orie", "Tumzie", "BieKay"]
for name in names:
    print(name)

# A character example to print the letters in a name one element at a time
print("Printing one letter of a name one character at a time")
name1 = "Orie"
for character in name1:
    print(character)